package ejemploClase;

public class Persona {
	
	private String nombre;
	private int edad;
	private String Apellidos;
	private double peso;
	private double altura;
	private double imc;
	
	public Persona(String nombre, int edad, String apellidos, double peso,
			double altura, double imc) {
		super();
		this.nombre = nombre;
		this.edad = edad;
		Apellidos = apellidos;
		this.peso = peso;
		this.altura = altura;
		this.imc = imc;
	}
	
	
	
	

}
