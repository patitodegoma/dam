
/*************************************
  Base de datos sobre líneas 
  de autobuses metropolitanos
*************************************/


/*************************
  CREACIÓN DE LAS TABLAS
*************************/

CREATE TABLE CONDUCTORES
(
  ID_COND       NUMBER(11) PRIMARY KEY,
  NOMBRE        VARCHAR2(200),
  DNI           VARCHAR2(9)
);
COMMENT ON TABLE CONDUCTORES IS 'Tabla con los datos de los diferentes conductores de los autobuses';
COMMENT ON COLUMN CONDUCTORES.ID_COND IS 'Clave primaria';



CREATE TABLE BUSES
(
  ID_BUS        NUMBER(11) PRIMARY KEY,
  MODELO        VARCHAR2(50),
  MATRICULA     VARCHAR2(7)
);

COMMENT ON TABLE BUSES IS 'Tabla con la flota de autobuses';
COMMENT ON COLUMN BUSES.ID_BUS IS 'Clave primaria';


CREATE TABLE LINEAS
(
  ID_LINEA      NUMBER(11) PRIMARY KEY,
  ORIGEN        VARCHAR2(50),
  DESTINO       VARCHAR2(50)
);

COMMENT ON TABLE LINEAS IS 'Tabla con las diferentes lineas o trayectores existentes';
COMMENT ON COLUMN LINEAS.ID_LINEA IS 'Clave primaria';


CREATE TABLE PARADAS
(
  ID_PARADA     NUMBER(11) PRIMARY KEY,
  NOMBRE        VARCHAR2(100),
  LOCALIDAD     VARCHAR2(50)
);

COMMENT ON TABLE PARADAS IS 'Tabla con las diferentes paradas que tienen todas las líneas';
COMMENT ON COLUMN PARADAS.ID_PARADA IS 'Clave primaria';


CREATE TABLE PARADA_LINEA
(
  ID_LINEA      NUMBER(11),
  ID_PARADA     NUMBER(11),
  PRIMARY KEY (ID_PARADA, ID_LINEA),
  FOREIGN KEY (ID_LINEA) REFERENCES LINEAS (ID_LINEA),
  FOREIGN KEY (ID_PARADA) REFERENCES PARADAS (ID_PARADA)
);
  
COMMENT ON TABLE PARADA_LINEA IS 'Tabla que conecta las diferentes lineas de una parada';
COMMENT ON COLUMN PARADA_LINEA.ID_PARADA IS 'Forma parte de la PK. FK (clave externa) a la tabla PARADAS';
COMMENT ON COLUMN PARADA_LINEA.ID_LINEA IS 'Forma parte de la PK. FK (clave externa) a la tabla LINEAS';

  
  
CREATE TABLE HORARIO_REGULAR
(
  ID_LINEA      NUMBER(11),
  HORA          NUMBER,
  MINUTOS       NUMBER,
  PRIMARY KEY (ID_LINEA, HORA, MINUTOS),
  FOREIGN KEY (ID_LINEA) REFERENCES LINEAS (ID_LINEA)
);

COMMENT ON TABLE HORARIO_REGULAR IS 'Tabla indica los horario de salida de los autobuses de la cabecera de cada linea';
COMMENT ON COLUMN HORARIO_REGULAR.ID_LINEA IS 'Forma parte de la PK. FK (clave externa) a la tabla LINEAS';

CREATE TABLE BUSES_LINEAS
(
  ID_BUS        NUMBER(11),
  ID_LINEA      NUMBER(11),
  ID_CONDUCTOR  NUMBER(11),
  PRIMARY KEY (ID_BUS, ID_LINEA, ID_CONDUCTOR),
  FOREIGN KEY (ID_BUS) REFERENCES BUSES (ID_BUS),
  FOREIGN KEY (ID_LINEA) REFERENCES LINEAS (ID_LINEA),
  FOREIGN KEY (ID_CONDUCTOR) REFERENCES CONDUCTORES (ID_COND)
);

COMMENT ON TABLE BUSES_LINEAS IS 'Tabla que conecta las diferentes lineas con los vehículos que se utilizan y el conductor encargado';
COMMENT ON COLUMN BUSES_LINEAS.ID_BUS IS 'Forma parte de la PK. FK (clave externa) a la tabla BUSES';
COMMENT ON COLUMN BUSES_LINEAS.ID_LINEA IS 'Forma parte de la PK. FK (clave externa) a la tabla LINEAS';
COMMENT ON COLUMN BUSES_LINEAS.ID_CONDUCTOR IS 'Forma parte de la PK. FK (clave externa) a la tabla CONDUCTORES';

